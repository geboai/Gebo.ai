package ai.gebo.security.model;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import org.springframework.security.core.annotation.AuthenticationPrincipal;

/**
 * Gebo.ai comment agent
 *
 * The @CurrentUser annotation is used to inject the current authenticated user 
 * principal into a method parameter or type in a Spring application.
 *
 * This custom annotation is meta-annotated with @AuthenticationPrincipal to work 
 * seamlessly with Spring Security.
 */
@Target({ElementType.PARAMETER, ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
@Documented
@AuthenticationPrincipal
public @interface CurrentUser {
    // This is a marker annotation with no methods
}