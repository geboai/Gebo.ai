/**
 * AI generated comments
 * This file contains TypeScript interfaces for managing chat interactions between users and Gebo.ai services.
 * It defines the structure for chat requests, responses, and complete chat sessions.
 */

import { GChatProfileConfiguration, GeboChatRequest, GeboChatResponse } from "@Gebo.ai/gebo-ai-rest-api";

/**
 * Represents a single interaction between a user and the chat system.
 * Contains both the user's request and the system's response in a paired structure.
 */
export interface ChatInteraction {
    /**
     * The user's request/input to the chat system
     */
    request:GeboChatRequest;
    
    /**
     * The response generated by the system to the user's request
     */
    response:GeboChatResponse;
}

/**
 * Represents a complete chat session that includes the configuration profile
 * and a history of all interactions that occurred during the session.
 */
export interface ChatSession {
    /**
     * Configuration profile that defines the behavior and settings for the chat
     */
    profile:GChatProfileConfiguration;
    
    /**
     * Array of all interactions (request/response pairs) in chronological order
     */
    interactions:ChatInteraction[];
};